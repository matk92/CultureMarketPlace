Nous vous remercions de faire confiance à CulturalMarketPlace. Notre objectif est de garantir votre satisfaction avec nos produits culturels. Veuillez lire attentivement notre politique de remboursement ci-dessous.

1. Conditions de Remboursement

Nous acceptons les demandes de remboursement dans les 10 jours suivant la réception de votre produit, sous réserve des conditions suivantes :

    Le produit doit être retourné dans son état d'origine, non utilisé, non endommagé et avec tous les accessoires.
    Les articles personnalisés ou en promotion ne sont généralement pas remboursables, sauf en cas de défaut ou de non-conformité.

2. Procédure de Remboursement

Pour demander un remboursement, veuillez suivre ces étapes :

a. Contactez notre service clientèle à CulturalMarketPlace@gmail.com dans les 10 jours suivant la réception du produit.

b. Expliquez la raison de votre demande de remboursement et fournissez des photos si nécessaire.

c. Notre équipe examinera votre demande et vous informera de l'approbation ou du rejet du remboursement.

d. Si votre demande est approuvée, suivez les instructions fournies pour retourner le produit.

e. Une fois que nous aurons reçu et inspecté le produit retourné, nous vous informerons de l'approbation définitive du remboursement.

3. Remboursements

Si votre remboursement est approuvé, le montant sera crédité sur le mode de paiement initial dans les 30 jours ouvrables.

4. Frais de Retour

Les frais de retour sont généralement à la charge du client, sauf en cas de produit défectueux ou de non-conformité de notre part.

5. Contact

Si vous avez des questions sur notre politique de remboursement, veuillez nous contacter à CulturalMarketPlace@gmail.com.

Nous vous remercions de votre compréhension et de votre coopération.

